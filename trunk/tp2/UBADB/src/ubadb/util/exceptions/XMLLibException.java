package ubadb.util.exceptions;


@SuppressWarnings("serial")
public class XMLLibException extends Exception{

	public XMLLibException() {
		super();
	}

	public XMLLibException(String arg0) {
		super(arg0);
	}

	public XMLLibException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public XMLLibException(Throwable arg0) {
		super(arg0);
	}

}
